    predicted = class_names[np.argmax(predictions[i])]
    actual = class_names[np.argmax(sample_labels[i])]

    color = 'green' if predicted == actual else 'red'
    plt.title(f"P: {predicted}\nA: {actual}", color=color, fontsize=8)
    plt.axis('off')

